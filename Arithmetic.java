class Arithmetic 
{
	public static void main(String[] args) 
	{
		int a =9;
		int b =2;
		System.out.print("Addition: ");
		System.out.println(a+b);
		System.out.print("Sub: ");
		System.out.println(a-b);
		System.out.print("Mul: ");
		System.out.println(a*b);
		System.out.print("Quot: ");
		System.out.println(a/b);
		System.out.print("Rem: ");
		System.out.println(a%b);
	}
}
